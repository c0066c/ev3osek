#ifndef MTYPES_H
#define MTYPES_H

/**
* \brief An unsigned 8 bit (1 byte) data type
*/
typedef unsigned char 	U8;
/**
* \brief An signed 8 bit (1 byte) data type
*/
typedef signed char 	S8;
/**
* \brief An unsigned 16 bit (2 byte) data type
*/
typedef unsigned short 	U16;
/**
* \brief An signed 16 bit (2 byte) data type
*/
typedef signed short 	S16;
/**
* \brief An unsigned 32 bit (4 byte) data type
*/
typedef unsigned long 	U32;
/**
* \brief An signed 32 bit (4 byte) data type
*/
typedef signed long 	S32;

#endif